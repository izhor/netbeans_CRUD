    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        String id = idLabel.getText();
        
        if(id == "None"){
            JOptionPane.showMessageDialog(null, "Failed To Update: ID value is None");
        }
        
        String sqlEdit = "UPDATE drug Set code=?, drug_name=?, drug_category=?, uom=?, buy_price=?, sell_price=?, stock=? WHERE id='"+id+"'";
        try{
            PreparedStatement stat = con.prepareStatement(sqlEdit);
            stat.setString(1, DrugCode.getText());
            stat.setString(2, DrugName.getText());
            stat.setString(3, DrugCategory.getSelectedItem().toString());
            stat.setString(4, uomInput.getText());
            stat.setString(5, buyPrice.getText());
            stat.setString(6, sellPrice.getText());
            stat.setString(7, stockInput.getText());
            stat.executeUpdate();
            if(id != "None"){
                JOptionPane.showMessageDialog(null, "Data Update Complete");
            }
            stat.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Data Update Failed: "+e);
        }
        dataTable();
        clearInput();
        
    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        String id = idLabel.getText();
        int confirmed = JOptionPane.showConfirmDialog(null,"Delete","Confirm Dialog",JOptionPane.YES_NO_OPTION);
        if(confirmed == 0 && id != "None"){
            try{
                String sqlDelete = "DELETE FROM drug WHERE id='"+id+"'";
                PreparedStatement stat = con.prepareStatement(sqlDelete);
                stat.executeUpdate();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Data Deletion Failed: "+e);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Deletion Failed: ID value is none");
        }
        dataTable();
        clearInput();
    }//GEN-LAST:event_deleteBtnActionPerformed

