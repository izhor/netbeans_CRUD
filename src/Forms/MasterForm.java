    protected void dataTable(){
        Object[] row = {"id","code","drug_name","Drug_Category","uom","buy_price","sell_price","stock"};
        tabMode = new DefaultTableModel(null, row);
        String search = searchData.getText();
        
        try{
            String sqlSearch = "SELECT * FROM drug where drug_name like '%"+search+"%' or drug_category like '%"+search+"%' order by id asc";
            Statement stat = con.createStatement();
            ResultSet res = stat.executeQuery(sqlSearch);
            while (res.next()){
          tabMode.addRow(new Object[]{
          res.getString(1),
          res.getString(2),
          res.getString(3),
          res.getString(4),
          res.getString(5),
          res.getString(6),
          res.getString(7),
          res.getString(8),
       });
      }
            
        DrugTable.setModel(tabMode);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Failure ocurred: "+e);
        }
    }
    
